import { imgPositionSizeMap } from '@/utils/imgSizeMap'

export default {
  data() {
    return {
      menusTab: 'doc'
    }
  },
  props: {
    type: {
      // æ˜¯ç›´æ’­é¡µè¿˜æ˜¯å¼•å¯¼é¡µ
      type: Number,
      default: 1
    },
     // ä¸»é¢˜èƒŒæ™¯
